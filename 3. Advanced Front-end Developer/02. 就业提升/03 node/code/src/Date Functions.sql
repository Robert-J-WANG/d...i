SELECT
	CURDATE() AS 'CURDATE',
	CURRENT_DATE() AS 'CURRENT_DATE',
	CURTIME() AS 'CURTIME',
	CURRENT_TIME() AS 'CURRENT_TIME',
	TIMESTAMPDIFF(YEAR, '2020-1-1', CURDATE()) AS 'TIMESTAMPDIFF';
SELECT
	TIMESTAMPDIFF(YEAR, '2020-1-1 1:1:1', '2021-1-1 1:1:1') AS 'YEAR 1',
	TIMESTAMPDIFF(YEAR, '2020-1-1 1:1:1', '2021-1-1 1:1:0') AS 'YEAR 2',
	TIMESTAMPDIFF(HOUR, '2020-1-1 1:1:1', '2020-1-1 1:1:1') AS 'HOUR 1',
	TIMESTAMPDIFF(HOUR, '2020-1-1 1:1:1', '2020-1-1 1:1:0') AS 'HOUR 2';
	
	
	
SELECT
	`name`,
	TIMESTAMPDIFF(YEAR,birthday,CURRENT_DATE()) as age,
	TIMESTAMPDIFF(YEAR,joinDate,CURRENT_DATE()) as work_years
FROM
	employee