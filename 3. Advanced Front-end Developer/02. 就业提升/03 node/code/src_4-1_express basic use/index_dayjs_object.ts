import dayjs from "dayjs";

/* ------------- 创建实例对象 ------------- */

// 1. 不传参数 - 获取当前日期和时间
console.log(dayjs());

/* ------- 2. 字符串 - 解析常见格式字符串 ------- */
console.log(dayjs("2025-1-1"));

/* ---------- 3. JS Date 对象 --------- */
console.log(dayjs(new Date()));

/* --------- 4. 时间戳 (毫秒) -------- */
console.log(dayjs(1000000000000));

/* ------------ 5.时间戳 (秒) ----------- */
console.log(dayjs(1000000000 * 1000));
